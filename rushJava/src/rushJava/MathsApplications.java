package rushJava;


public class MathsApplications {

	public static void main(String[] args) {
		
		// Math.max büyük olaný seçer
		System.out.println(Math.max(5,10));
		
		// Math.min küçük olaný seçer
		System.out.println(Math.min(5, 1));
		
		// Math.sqrt karekök alýr
		System.out.println(Math.sqrt(36));
		
		// Math.abs pozitif sayýya çevirir
		System.out.println(Math.abs(-5.0));
		
		// Math.random rastgele sayý üretilir
		System.out.println(Math.random());
		
		// istedigimiz aralýkta random sayý üretmek için
		int randomNumber = (int)(Math.random() * 101); // 0-100 arasý

	
	
	}

}
